var s1 =document.getElementById("s1");
var p1 = document.getElementById("play1");

p1.onclick = function() {
  if( s1.paused){
    s1.play();
    p1.src="images/pause.png";
  }
  else{
  s1.pause();
  p1.src="images/play.png";}
}

